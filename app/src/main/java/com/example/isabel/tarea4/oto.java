package com.example.isabel.tarea4;

public class oto {
    private int imageResurce;
    private String title;
    private int color;

    public oto(int imageResurce, String title, int color) {
        this.imageResurce = imageResurce;
        this.title = title;
        this.color = color;
    }

    public int getImageResurce() {
        return imageResurce;
    }

    public String getTitle() {
        return title;
    }

    public int getColor() {
        return color;
    }
}
